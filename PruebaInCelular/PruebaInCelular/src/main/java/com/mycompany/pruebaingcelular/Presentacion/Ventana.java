    private void recargabottonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_recargabottonActionPerformed
        int valor = Integer.parseInt(ValorRecargaField.getText());
        String numero = numeroRecargaField.getText();

        CelularJpaController celularJpa = new CelularJpaController();
        RecargasJpaController recargaJpa = new RecargasJpaController();

        Celular celular = celularJpa.ValidarCelular(numero);

        if (celular != null) {
            if (!celular.getEstado().equalsIgnoreCase("Activo")) {
                JOptionPane.showMessageDialog(this, "El celular está inactivo. No se puede realizar la recarga.");
                return;
            }

            // Distribución automática: 2/3 para saldo, 1/3 para megas (5 GB por dólar)
            double saldoRecarga = (2.0 / 3.0) * valor;
            double megasRecarga = (1.0 / 3.0) * valor * 5;

            // Registrar la recarga
            Recargas recarga = new Recargas();
            recarga.setValor(valor);
            recarga.setSaldo(saldoRecarga);
            recarga.setMegas(megasRecarga);
            recarga.setCelular(celular);

            try {
                recargaJpa.create(recarga);

                // Actualizar el celular
                celular.setSaldo(celular.getSaldo() + saldoRecarga);
                celular.setMegas(celular.getMegas() + megasRecarga);
                celularJpa.edit(celular);

                JOptionPane.showMessageDialog(this, "✅ Recarga realizada con éxito.\nNuevo saldo: $"
                        + celular.getSaldo() + "\nNuevas megas: " + celular.getMegas() + " GB");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "❌ Error al registrar la recarga: " + e.getMessage());
                e.printStackTrace();
            }

        } else {
            JOptionPane.showMessageDialog(this, "❌ El número no está registrado.");
        }
    

    }//GEN-LAST:event_recargabottonActionPerformed

