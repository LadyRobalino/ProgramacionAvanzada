    private void ingresarNumeroButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ingresarNumeroButtonActionPerformed
        String numero = numeroField.getText();
        String estado = estadoField.getText();
        double saldo = Double.parseDouble(saldoField.getText());
        double megas = Double.parseDouble(megasField.getText());

        Celular celular = new Celular();
        celular.setNumero(numero);
        celular.setEstado(estado);
        celular.setSaldo(saldo);
        celular.setMegas(megas);
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("com.mycompany_Examen1_jar_1.0-SNAPSHOTPU");
        EntityManager em = emf.createEntityManager();
        try {
            em.getTransaction().begin();
            em.persist(celular);         // Luego el turno (con referencia al vehículo)
            em.getTransaction().commit();
            JOptionPane.showMessageDialog(this, "¡Datos guardados exitosamente!");
        } catch (Exception e) {
            em.getTransaction().rollback();
            JOptionPane.showMessageDialog(this, "Error al guardar: " + e.getMessage());
            e.printStackTrace();
        } finally {
            em.close();
            emf.close();
        }

    }//GEN-LAST:event_ingresarNumeroButtonActionPerformed

